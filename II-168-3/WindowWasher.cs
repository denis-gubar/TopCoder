	public int fastest( int width, int height, int[] washTimes )
	{
		int[] work = new int[washTimes.Length];
		for( int i = 0; i < width; i++ )
		{
			int min = work[0] + washTimes[0];
			int x = 0;
			for( int j = 1; j < washTimes.Length; j++ )
			{
				if ( min > work[j] + washTimes[j] )
				{
					min = work[j] + washTimes[j];
					x = j;
				}
			}
			work[x] += washTimes[x];
		}
		Array.Sort( work );
		Array.Reverse( work );
		return work[0] * height;
	}

