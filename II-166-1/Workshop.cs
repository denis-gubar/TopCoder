	public int pictureFrames( int[] pieces )
	{
		int n = pieces.Length;
		int result = 0;
		for( int x = 0; x < n - 2; x++ )
		{
			for( int y = x + 1; y < n - 1; y++ )
			{
				for( int z = y + 1; z < n; z++ )
				{
					if ( pieces[x] < pieces[y] + pieces[z] && 
						pieces[y] < pieces[x] + pieces[z] && 
						pieces[z] < pieces[x] + pieces[y] )
					{
						result++;
					}
				}
			}
		}
		return result;
	}

