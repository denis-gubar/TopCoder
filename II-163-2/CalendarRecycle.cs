	{
		return (year % 4 == 0) && (year % 100 != 0 || year % 400 == 0);
	}
	public int useAgain( int year )
	{
		int i = year;
		int r = 0;
		while( true )
		{
			i++;
			r += isLeap( i ) ? 2 : 1;
			if ( r % 7 == 0 && isLeap( i ) == isLeap( year ) ) return i;
		}		
	}

