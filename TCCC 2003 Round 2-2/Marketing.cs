	bool searchCycle( int k )
	{
		string[] x = compete[k].Split();
		for( int i = 0; i < n; i++ )
		{
			string[] y = compete[i].Split();
			bool flag = false;
			for( int j = 0; j < x.Length; j++ ) flag |= x[j].CompareTo( i.ToString() ) == 0;
			for( int j = 0; j < y.Length; j++ ) flag |= y[j].CompareTo( k.ToString() ) == 0;
			if ( flag )
			{
				if ( M[i] == 0 )
				{
					M[i] = -M[k];
					if( !searchCycle( i ) ) return false;
				}
				else if ( M[i] == M[k] ) return false;
			}
		}
		return true;
	}

	public long howMany( string[] compete )
	{
		this.compete = compete;
		n = compete.Length;
		M = new int[n];
		int result = 0;
		for( int i = 0; i < n; i++ )
		{
			if ( M[i] == 0 )
			{				
				M[i] = 1;
				if ( !searchCycle( i ) ) return -1;
				result++;
			}
		}
		return (long) 1 << result;
	}

