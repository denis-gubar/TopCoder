	int gcd( int a, int b )
	{
		while( a > 0 && b > 0 )
		{
			if ( a > b ) a %= b;
			else b %= a;
		}
		return a > b ? a : b;
	}
	int lcm_( int a, int b )
	{
		return a / gcd( a, b ) * b;
	}
	public int lcm( int first, int last )
	{
		int result = 1;
		for( int i = first; i <= last; i++ )
		{
			result = lcm_( result, i );
		}
		return result;
	}

