	public int partCount( string original, string compound )
	{
		int n = compound.Length;
		if ( n == 0 ) return 0;
		int[] D = new int[n + 1];
		for( int i = 0; i <= n; i++ ) D[i] = n + 1;
		for( int i = 0; i < n; i++ )
		{
			for( int k = 1; i + k <= n; k++ )
			{
				string s = compound.Substring( i, k );
				if ( original.StartsWith( s ) || original.EndsWith( s ) )
				{
					if ( i == 0 ) D[i + k] = 1;
					if ( D[i + k] > D[i] + 1 ) D[i + k] = D[i] + 1;
				}
			}
		}
		return D[n] <= n ? D[n] : -1;
	}

