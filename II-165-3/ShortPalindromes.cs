	public string shortest( string s )
	{
		string[,] M = new string[s.Length, s.Length];
		for( int i = 0; i < s.Length; i++ )
		{
			M[i, i] = s[i].ToString();
		}
		for( int k = 1; k < s.Length; k++ )
		{
			for( int i = 0; i + k < s.Length; i++ )
			{
				if ( s[i] == s[i + k] )
				{
					M[i, i + k] = s[i] + M[i + 1, i + k - 1] + s[i];
				}
				else
				{
					string s1 = s[i] + M[i + 1, i + k] + s[i];
					string s2 = s[i + k] + M[i, i + k - 1] + s[i + k];
					if ( s1.Length < s2.Length ) M[i, i + k] = s1;
					if ( s2.Length < s1.Length ) M[i, i + k] = s2;
					if ( s1.Length == s2.Length ) M[i, i + k] = s1.CompareTo( s2 ) < 0 ? s1 : s2;				
				}
			}
		}
		return M[0, s.Length - 1];
	}

