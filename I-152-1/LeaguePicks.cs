	public int[] returnPicks( int position, int friends, int picks ) 
	{
		ArrayList A = new ArrayList();
		for( int i = 0; i * friends <= picks; i++ )
		{
			int x = (i % 2 == 0) ? i * friends + position : (i + 1) * friends - position + 1;
			if ( x <= picks ) A.Add( x );
		}
		return (int[]) A.ToArray( 1.GetType() );
	}

