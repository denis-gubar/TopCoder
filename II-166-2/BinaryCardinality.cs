	public int[] arrange( int[] numbers ) 
	{
		int n = numbers.Length;
		Pair[] x = new Pair[n];
		for( int i = 0; i < n; i++ )
		{
			int y = numbers[i];
			int z = 0;
			while( y > 0 )
			{
				y &= y - 1;
				z++;
			}
			x[i] = new Pair( z, numbers[i] );
		}
		Array.Sort( x );
		for( int i = 0; i < n; i++ )
			numbers[i] = x[i].i;
		return numbers;
	}

