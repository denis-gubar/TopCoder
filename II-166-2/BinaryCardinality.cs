class Pair:IComparable
{
	public Pair(int rank, int i)
	{
		this.i = i;
		this.rank = rank;
	}
	public int CompareTo( object o )
	{
		Pair a = (Pair) o;
		if ( rank.CompareTo( a.rank ) != 0 ) return rank.CompareTo( a.rank );
		return i.CompareTo( a.i );
	}

	public int i;
	public int rank;
}

