	public int howMany( int size, string[] subsets ) 
	{
		Queue Q = new Queue();
		ArrayList A = new ArrayList();
		bool[] flag = new bool[1 << size];
		int result = 0;
		for( int i = 0; i < subsets.Length; i++ )
		{
			string[] x = subsets[i].Split();
			int k = 0;
			if ( subsets[i] != "" )
				for( int j = 0; j < x.Length; j++ )
					k |= 1 << (int.Parse( x[j] ) - 1 );
			if ( !flag[k] )
			{
				flag[k] = true;
				Q.Enqueue( k );
			}
		}
		while( Q.Count > 0 )
		{
			int k = (int) Q.Dequeue();
			int a = ((1 << size) - 1) ^ k;
			if ( !flag[a] )
			{
				flag[a] = true;
				Q.Enqueue( a );
			}
			for( int i = 0; i < A.Count; i++ )
			{
				int b = (int) A[i] | k;
				if ( !flag[b] )
				{
					flag[b] = true;
					Q.Enqueue( b );
				}
			}
			A.Add( k );
		}
		for( int i = 0; i < 1 << size; i++ )
			if ( flag[i] )
				result++;
		return result;
	}

