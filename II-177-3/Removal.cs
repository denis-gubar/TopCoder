	public int finalPos( int n, int k, string[] remove ) 
	{
		long a = k;
		for( int i = remove.Length - 1; i >= 0; i-- )
		{
			string[] x = remove[i].Split( '-' );
			int low = int.Parse( x[0] );
			int high = int.Parse( x[1] );
			if ( a >= low ) a += high - low + 1;
		}
		return ((int) a ) > n ? -1 : (int) a;
	}

