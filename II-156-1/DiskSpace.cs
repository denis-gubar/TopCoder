	public int minDrives( int[] used, int[] total )
	{
		Array.Sort( total );
		Array.Reverse( total );
		int result = 0;
		for( int i = 0; i < used.Length; i++ )
		{
			while ( used[i] > 0 )
			{
				if ( used[i] <= total[result] ) 
				{
					total[result] -= used[i];
					used[i] = 0;
				}
				else used[i] -= total[result++];
			}
		}
		return result + 1;
	}

