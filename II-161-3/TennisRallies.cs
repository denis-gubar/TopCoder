	public int howMany( int numLength, string[] forbidden, int allowed ) 
	{
		int n = forbidden.Length;
		int result = 0;
		for( int i = 0; i < 1 << numLength; i++ )
		{
			int count = 0;
			for( int j = 0; j < n; j++ )
				for( int l = 0; count < allowed && l + forbidden[j].Length <= numLength; l++ )
				{
					bool flag = true;
					for( int k = l; flag && k < l + forbidden[j].Length; k++ )
						flag = (((1 << k) & i) > 0) ^ (forbidden[j][k - l] == 'c' );
					if ( flag )
						count++;
				}
			if ( count < allowed )
				result++;
		}
		return result;
	}

