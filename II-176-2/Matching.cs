	{
		string[] A = new string[] {"CIRCLE", "SQUIGGLE", "DIAMOND", 
									"RED", "BLUE", "GREEN",
									"SOLID", "STRIPED", "EMPTY",
									"ONE", "TWO", "THREE"};
		string[] result = new string[4];
		for( int i = 0; i < 4; i++ )
			if ( first[i].CompareTo( second[i] ) == 0 ) result[i] = first[i];
			else result[i] = A[i * 3 + 3 - Array.IndexOf( A, first[i] ) % 3 - Array.IndexOf( A, second[i] ) % 3];
		return result;
	}

