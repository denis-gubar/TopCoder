	public string[] logo( int size, char background ) 
	{
		string[] result = new string[2 * size - 1];
		result[2 * size - 2] = result[0] 
			= new string( background, size - 1 ) + "X" + new string( background, size - 1 );
		result[size - 1] = "X" + new string( ' ', 2 * size - 3 ) + "X";
		for( int i = 1; i < size; i++ )
		{
			result[i] = result[2 * size - 2 - i] 
				= new string( background, size - i - 1 ) + "X" + new string( ' ', 2 * i - 1 ) +
				"X" + new string( background, size - i - 1 );
		}
		return result;
	}

