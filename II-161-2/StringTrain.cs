	public string buildTrain( string[] cars )
	{
		string s = cars[0];
		bool[] flag = new bool[256];
		for( int i = 1; i < cars.Length; i++ )
		{
			for( int j = cars[i].Length - 1; j > 0; j-- )
			{
				if ( s.Substring( 1 ).EndsWith( cars[i].Substring( 0, j ) ) )
				{
					s += cars[i].Substring( j );
					break;
				}
			}
		}
		string result = "";
		for( int i = s.Length - 1; i >= 0; i-- )
		{
			if ( !flag[(int) s[i]] )
			{
				flag[(int) s[i]] = true;
				result = s[i] + result;
			}
		}
		return s.Length.ToString() + " " + result;
	}

