	int gcd( int a, int b )
	{
		while ( a > 0 && b > 0 )
		{
			if ( a > b ) a = a % b;
			else b = b % a;
		}
		return a > b ? a : b;
	}
	public int lunchtime( int branch, int rest, int leaf )
	{
		return branch / ( rest * leaf / gcd(rest, leaf) ) + 1;
	}

