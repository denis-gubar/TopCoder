	public string[] seating( string[] attendees )
	{
		int n = attendees.Length;
		if ( n % 4 != 0 ) return new string[] {};
		string[] boys = new string[n / 2];
		string[] girls = new string[n / 2];
		int b = 0;
		int g = 0;
		for( int i = 0; i < n; i++ )
		{
			string[] x = attendees[i].Split();
			if ( x[1] == "boy" )
			{
				if ( b >= n / 2 ) return new string[] {};
				boys[b++] = x[0];
			}
			if ( x[1] == "girl" )
			{
				if ( g >= n / 2 ) return new string[] {};
				girls[g++] = x[0];
			}
		}
		Array.Sort( boys );
		Array.Sort( girls );
		string[] result = new string[n + 2];
		result[0] = "HOST";
		for( int i = 0; i < n / 4; i++ )
		{
			result[2 * i + 1] = girls[i];
			result[2 * i + 2] = boys[i];
		}
		result[n / 2 + 1] = "HOSTESS";
		for( int i = n / 4; i < n / 2; i++ )
		{
			result[2 * i + 2] = boys[i];
			result[2 * i + 3] = girls[i];			
		}
		return result;
	}

