    public double cycleRange( double R )
    {
		double x = 0.25;
		for( int i = 0; i < 200000; i++ )
		{
			x = R * x * (1 - x);
		}
		double min = x;
		double max = x;
		for( int i = 0; i < 1000; i++ )
		{
			x = R * x * (1 - x);
			if ( min > x ) min = x;
			if ( max < x ) max = x;
		}
        return max - min;
    }

