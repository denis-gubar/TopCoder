	private int convert( string s, int n )
	{
		string digits = "0123456789ABCDEFGHIJ";
		int result = 0;
		for( int i = 0; i < s.Length; i++ )
		{
			if ( digits.IndexOf( s[i] ) >= n ) return -1;
			result = result * n + digits.IndexOf( s[i] );
		}
		return result;
	}
	public int[] getBase( string equation ) 
	{
		ArrayList A = new ArrayList();
		string[] x = equation.Split( "+=".ToCharArray() );		
		for( int i = 2; i < 21; i++ )
		{
			int a = convert( x[0], i );
			int b = convert( x[1], i );
			int c = convert( x[2], i );
			if ( a >= 0 && b >= 0 && c >= 0 && a + b == c ) A.Add( i );
		}
		return (int[]) A.ToArray( 1.GetType() );
	}

