    public int maxPoints( int[] toss )
    {
		int[] a = new int[7];
		int max = 0;
		for( int i = 0; i < toss.Length; i++ )
		{
			a[toss[i]]++;
		}
		max = a[1];
		for( int i = 2; i < 7; i++ )
		{
			max = max > i * a[i] ? max : i * a[i];
		}
        return max;
    }

