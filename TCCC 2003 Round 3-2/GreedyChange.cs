	public int smallest( int[] denominations )
	{
		int n = denominations.Length;
		Array.Sort( denominations );
		Array.Reverse( denominations );
		int m = denominations[0];
		int[] M = new int[2 * m];
		for( int i = 1; i < 2 * m; i++ )
		{
			int x = i;
			int k = 0;
			int count = 0;
			while( x > 0 )
			{
				if ( x >= denominations[k] )
				{
					count += x / denominations[k];
					x %= denominations[k];
				}
				else k++;
			}
			int min = i;
			k = n - 1;
			while( k >= 0 && denominations[k] <= i )
			{
				if ( min > M[i - denominations[k]] ) min = M[i - denominations[k]] + 1;
				k--;
			}
			M[i] = min;
			if ( min != count ) return i;
		}
		return -1;
	}

