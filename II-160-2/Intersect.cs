	public int area( int[] x, int[] y )
	{
		Array.Sort( x, 0, 2 );
		Array.Sort( y, 0, 2 );
		for( int i = 2; i < x.Length; i += 2 )
		{
			Array.Sort( x, i, 2 );
			Array.Sort( y, i, 2 );
			if ( x[i] > x[0] ) x[0] = x[i];
			if ( x[0] >= x[1] ) return 0;
			if ( x[i + 1] < x[1] ) x[1] = x[i + 1];
			if ( x[0] >= x[1] ) return 0;
			if ( y[i] > y[0] ) y[0] = y[i];
			if ( y[0] >= y[1] ) return 0;
			if ( y[i + 1] < y[1] ) y[1] = y[i + 1];
			if ( y[0] >= y[1] ) return 0;
		}		
		return (x[1] - x[0]) * (y[1] - y[0]);
	}

