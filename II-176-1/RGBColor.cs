	{
		bool flag = true;
		for( int i = 0; i < 3; i++ )
			flag &= Math.Abs( rgb[i] - (255 - rgb[i]) ) < 33;
		if ( flag )
			for( int i = 0; i < 3; i++ )
				rgb[i] += rgb[i] < 128 ? 128 : -128;
		else
			for( int i = 0; i < 3; i++ )
				rgb[i] = 255 - rgb[i];
		return rgb;
	}

