	public int stridesTaken( int[] flights, int stairsPerStride )
	{
		int result = 0;
		for( int i = 0; i < flights.Length; i++ )
		{
			result += (flights[i] - 1) / stairsPerStride + 1;
		}
		result += 2 * (flights.Length - 1);
		return result;
	}

