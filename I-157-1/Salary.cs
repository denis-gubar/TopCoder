	public int howMuch( string[] arrival, string[] departure, int wage ) 
	{
		double result = 0;
		for( int i = 0; i < arrival.Length; i++ )
		{
			string[] x = arrival[i].Split( ':' );
			int start = int.Parse( x[0] ) * 3600 + int.Parse( x[1] ) * 60 + int.Parse( x[2] );
			x = departure[i].Split( ':' );
			int finish = int.Parse( x[0] ) * 3600 + int.Parse( x[1] ) * 60 + int.Parse( x[2] );
			while( start < finish )
			{				
				result += 1.0;
				if ( start < 6 * 3600 || start >= 18 * 3600 )
					result += 0.5;
				start++;
				start %= 24 * 3600;
			}
		}
		return (int) (result * wage / 3600);
	}

