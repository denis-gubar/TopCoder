			{
				M[i * glass1 + j * glass2] = true;
				for( int k = 0; k < 10; k++ )
				{
					M[i * glass1 + k * Math.Abs(i * glass1 - j * glass2)] = true;
				}
			}
		}
		for( int i = 0; i < 10; i++ )
		{
			while( !M[x] ) x++;
			result[i] = x++;
		}
		return result;
	}

