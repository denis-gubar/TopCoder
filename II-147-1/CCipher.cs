    public string decode( string cipherText, int shift )
    {
		string result = "";
		for( int i = 0; i < cipherText.Length; i++ )
		{
			int k = (int)(cipherText[i]) - shift;
			if ( k < (int) 'A' ) k += 26;
			result += (char) k;
		}
        return result;
    }

