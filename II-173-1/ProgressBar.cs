	public string showProgress( int[] taskTimes, int tasksCompleted ) 
	{
		int total = 0;
		for( int i = 0; i < taskTimes.Length; i++ )
			total += taskTimes[i];
		int completed = 0;
		for( int i = 0; i < tasksCompleted; i++ )
			completed += taskTimes[i];
		int x = completed * 20 / total;
		return new string( '#', x ) + new string( '.', 20 - x );
	}

