	public int calculate( string input ) 
	{
		string[] x = input.Split( "+-/*".ToCharArray() );
		int a = int.Parse( x[0] );
		int b = int.Parse( x[1] );
		if ( input.IndexOf( "+" ) > -1 ) return a + b;
		if ( input.IndexOf( "-" ) > -1 ) return a - b;
		if ( input.IndexOf( "*" ) > -1 ) return a * b;
		return a / b;
	}

