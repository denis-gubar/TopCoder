	int gcd( int a, int b )
	{
		while( a > 0 && b > 0 )
		{
			if ( a > b ) a %= b;
			else b %= a;
		}
		return Math.Max( a, b );
	}

	int col( int x )
	{
		int result = 0;
		while( x > 0 )
		{
			result++;
			x -= result;
		}
		return x + result;
	}

	int row( int x )
	{
		int result = 0;
		while( x > 0 )
		{
			result++;
			x -= result;
		}
		return result;
	}

	public int calc( int glass, int units )
	{
		if( glass == 1 ) return units;
		int c = col( glass );
		int r = row( glass );
		int result = 0;
		if ( c > 1 ) result += Math.Max( 0, calc( glass - r, units ) - (1 << (r - 1)) );
		if ( c < r ) result += Math.Max( 0, calc( glass - r + 1, units ) - (1 << (r - 1)) );
		return result;
	}

	public string howMuch( int height, int glass, int units )
	{
		int x = calc( glass, units );
		int y = 1 << row( glass );
		int k = gcd( x, y );
		x /= k; y /= k;
		if ( x > y ) return "1/1";
		return x.ToString() + "/" + y.ToString();
	}

