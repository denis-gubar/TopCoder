	public int[] digits( int b )
	{
		ArrayList A = new ArrayList();
		for( int i = 2; i < b; i++ )
		{
			bool flag = false;
			for( int j = 1; j < b * b; j++ )
			{
				if ( j * b % i == 0 )
				{
					int x = j * b;
					int c = 0;
					while( x > 0 )
					{
						c += x % b;
						x /= b;
					}
					flag = c % i != 0;
					if ( flag ) break;
				}
			}
			if ( !flag ) A.Add( i );
		}
		return (int[]) A.ToArray( b.GetType() );
	}

