	public int[] getSwimTimes( int[] distances, int[] speeds, int current )
	{
		for( int i = 0; i < distances.Length; i++ )
		{
			if ( distances[i] == 0 ) continue;
			if ( speeds[i] <= current ) distances[i] = -1;
			else
			{
				distances[i] = (int) (1.0 * distances[i] / (speeds[i] - current) + 1.0 * distances[i] / (speeds[i] + current));
			}
		}
		return distances;
	}

