    public int minCuts( int[] sections )
    {
		int result = 0;
		Array.Sort( sections );
		int j = 0;
		for( int i = 0; i < sections.Length; i++ )
		{
			if ( sections[j] > 0 )
			{
				result += 1;
				sections[j]--;
			}
			else
			{
				j++;
			}
		}
        return result;
    }

