	public int avgMinutes( string[] times ) 
	{
	    int result = 0;
		for( int i = 0; i < times.Length; i++ )
		{
			int time = int.Parse( times[i].Substring(0, 2) ) * 60 + 
				int.Parse( times[i].Substring(3, 2) );
			time %= 12 * 60;
			if ( times[i][6] == 'P' )
				time += 12 * 60;
			time += 1440 * (int.Parse( times[i].Substring( 14 ) ) - 1);
			result += time - 8 * 60;
		}
		return (2 * result / times.Length + 1) / 2;
	}

