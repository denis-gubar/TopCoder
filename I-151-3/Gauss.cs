	public string[] whichSums( string target ) 
	{
		ArrayList A = new ArrayList();
		long x = long.Parse( target );
		for( long i = 2; i < Math.Sqrt( 2 * x ) + 1; i++ )
			if ( 2 * x % i == 0 )
			{
				long j = 2 * x / i;
				if ( (i + j - 1) % 2 == 0 )
				{
					long b = (i + j - 1) / 2;
					long a = j - b;
					if ( a > 0 )
						A.Add( "[" + a.ToString() + ", " + b.ToString() + "]" );
				}
			}
		A.Reverse();
		return (string[]) A.ToArray( "".GetType() );
	}

