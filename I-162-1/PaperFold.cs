	private int calc( int[] paper, int[] box, int i )
	{
		if ( i > 8 ) return -1;
		Array.Sort( paper );
		Array.Sort( box );
		if ( paper[0] <= box[0] && paper[1] <= box[1] ) return i;
		int a = calc( paper, new int[] { box[0], box[1] * 2 }, i + 1 );
		int b = calc( paper, new int[] { box[1], box[0] * 2 }, i + 1 );
		if ( a == -1 ) return b;
		if ( b == -1 ) return a;
		return Math.Min( a, b );
	}
	public int numFolds( int[] paper, int[] box )
	{
		return calc( paper, box, 0 );
	}

