	int gcd( int a, int b )
	{
		while( a > 0 && b > 0 )
			if ( a > b )
				a %= b;
			else
				b %= a;
		return a + b;
	}

	int lcm( int a, int b )
	{
		return a / gcd( a, b ) * b;
	}

	void calc( int sum, int[] M, int i, int x )
	{
		int result = 1;
		M[i] = x;
		if ( sum == 0 )
			for( int j = 1; j <= i; j++ )
				 result = lcm( result, M[j] );
		for( int j = Math.Max( x, 1 ); j <= sum; j++ )
			calc( sum - j, M, i + 1, j );
		if ( max < result )
		{
			max = result;
			maxResult = new int[i];
			Array.Copy( M, 1, maxResult, 0, i );
		}
	}

	int max = 0;
