	public string best( int n )
	{
		int[] M = new int[n + 1];
		calc( n, M, 0, 0 );
		string alphabet = "ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz";
		string result = "";
		int k = 0;
		for( int i = 0; i < maxResult.Length; i++ )
		{
			if ( maxResult[i] > 1 )
				result += alphabet[k + 1];
			for( int j = 1; j < maxResult[i] - 1; j++ )
				result += alphabet[k + j + 1];
			result += alphabet[k];
			k += maxResult[i];
		}
		return result;
	}

