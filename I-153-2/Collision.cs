	{
		int count = ids;
		double x = 1;
		double y = 1;
		for( int i = 0; i < assignments.Length; i++ )
		{
			if ( assignments[i] > ids ) return 0;
			for( int j = 0; j < assignments[i]; j++ )
			{
				x *= (double) count / ids;
				y *= (double) count-- / (ids - j);
			}
		}
		return y - x;
	}

