	public int[] nextTree( string[] orchard )
	{
		int n = orchard.Length;		
		int max = -1;
		int a = 0;
		int b = 0;
		for( int i = 0; i < n; i++ )
		{
			for( int j = 0; j < n; j++ )
			{
				int k = max + 1;
				while( true )
				{
					bool flag = false;
					for( int x = i - k; x <= i + k; x++ )
					{
						for( int y = j - k; y <= j + k; y++ )
						{
							if ( Math.Abs(i - x) + Math.Abs(j - y) <= k && 
								(x < 0 || y < 0 || x >= n || y >= n || orchard[x][y] == 'T') )
							{
								flag = true; break;
							}
						}
						if ( flag ) break;
					}
					if ( flag ) break;
					max = k++; a = i; b = j;
				}
			}
		}
		return new int[] {a + 1, b + 1};
	}

