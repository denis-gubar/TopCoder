	public string outcome( string candidates, string[] ballots ) 
	{
		if ( candidates == "" ) return "";
		int[] count = new int[256];
		int total = 0;
		for( int i = 0; i < ballots.Length; i++ )
		{
			if ( ballots[i].Length > 0 ) 
			{
				count[ballots[i][0]]++;
				total++;
			}
		}
		int min = int.MaxValue;
		foreach( char c in candidates )
		{
			if ( count[c] > total / 2 ) return c.ToString();
			if ( min > count[c] ) min = count[c];
		}
		string newCandidates = candidates;
		foreach( char c in candidates )
			if ( count[c] == min )
			{
				for( int i = 0; i < ballots.Length; i++ )
				{
					ballots[i] = ballots[i].Replace( c.ToString(), "" );
				}
				newCandidates = newCandidates.Replace( c.ToString(), "" );
			}
		return outcome( newCandidates, ballots );
	}

