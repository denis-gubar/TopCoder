	public double nextAgreement( string trueTime, string skewTime, int hourlyGain ) 
	{
		string[] x1 = trueTime.Split( ':' );
		string[] x2 = skewTime.Split( ':' );
		int t1 = int.Parse( x1[0] ) * 60 * 60 + int.Parse( x1[1] ) * 60 + int.Parse( x1[2] );
		int t2 = int.Parse( x2[0] ) * 60 * 60 + int.Parse( x2[1] ) * 60 + int.Parse( x2[2] );
		int delta = (12 * 60 * 60 + t1 - t2) % (12 * 60 * 60);
		if (delta == 0) return 0.0;
		if ( hourlyGain < 0 ) delta = (12 * 60 * 60) - delta;
		return 1.0 * delta / Math.Abs(hourlyGain);
	}

