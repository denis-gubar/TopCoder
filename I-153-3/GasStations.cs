	public double tripCost( int[] dist, int[] price, int mpg, int tankSize, int tripLength ) 
	{
		double[] M = new double[tripLength];
		double result = 0;
		for( int i = mpg * tankSize; i < tripLength; i++ ) M[i] = double.MaxValue;
		for( int i = 0; i < dist.Length; i++ )
		{
			for( int j = 0; j < mpg * tankSize && dist[i] + j < tripLength; j++ )
			{
				if ( M[dist[i] + j] > (double) price[i] / mpg )
					M[dist[i] + j] = (double) price[i] / mpg;
			}
		}
		for( int i = 0; i < tripLength; i++ ) result += M[i];
		return result;
	}

