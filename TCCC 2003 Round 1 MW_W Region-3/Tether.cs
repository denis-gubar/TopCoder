	public int deadTrees( int rope, int radius, int[] x, int[] y )
	{
		int result = 0;
		for( int i = 0; i < x.Length; i++ )
		{
			x[i] = Math.Abs( x[i] );
			if ( y[i] <= -radius )
			{
				if ( (y[i] + radius)*(y[i] + radius) + x[i]*x[i] <= rope*rope ) result++;
				continue;
			}
			double b = x[i]*x[i] + y[i]*y[i] - radius*radius;
			double alpha = Math.Atan( Math.Sqrt( b ) / radius );
			double beta = 0;
			double a = 0;
			if ( y[i] < 0 )
			{
				beta = -Math.Atan( (double) x[i] / y[i] );
				a = (Math.PI - alpha - beta) * radius;
			}
			if ( y[i] > 0 )
			{
				beta = Math.Atan( (double) x[i] / y[i] );
				a = (Math.PI - alpha - beta) * radius;
			}
			if ( a + Math.Sqrt( b ) <= rope ) result++;
		}
		return result;
	}

